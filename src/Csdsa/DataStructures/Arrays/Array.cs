namespace Csdsa.DataStructures.Arrays;

/// <summary>
/// Provides array-related helper algorithms and utilities.
/// <para>
/// Concepts:
/// </para>
/// <list type="bullet">
///   <item>
///     <description>
///     Fixed size arrays are declared with <c>new</c> and allocated contiguously in memory.
///     </description>
///   </item>
///   <item>
///     <description>
///     Index-based access supports O(1) reads and writes.
///     </description>
///   </item>
///   <item>
///     <description>
///     Insert and delete operations can be O(n) in the worst case due to shifting elements.
///     </description>
///   </item>
///   <item>
///     <description>
///     Multidimensional arrays use <c>[,]</c>, while jagged arrays use <c>[][].</c>
///     </description>
///   </item>
///   <item>
///     <description>
///     Dynamic arrays are resizable linear containers with amortized O(1) appends.
///     </description>
///   </item>
/// </list>
/// <para>
/// Key practices supported by this module include:
/// </para>
/// <list type="bullet">
///   <item>
///     <description>Rotating arrays left and right (without extra space).</description>
///   </item>
///   <item>
///     <description>Finding maximum and minimum elements.</description>
///   </item>
///   <item>
///     <description>Linear and binary search over arrays.</description>
///   </item>
///   <item>
///     <description>Merging two sorted arrays into a single sorted result.</description>
///   </item>
///   <item>
///     <description>Flattening two-dimensional rectangular arrays into one-dimensional arrays.</description>
///   </item>
///   <item>
///     <description>In-place generic array reversal.</description>
///   </item>
/// </list>
/// </summary>
